package day14.sub2;

import static java.lang.Math.*;

public class Ex01 {
	public static void main(String[] args) {
		// java.util.Math - 수학과 관련된 유용한 기능
		
		int num = -10;
		System.out.println(abs(num));
		
		double num2 = 3.6;
		System.out.println(ceil(num2));
		System.out.println(floor(num2));
		System.out.println(round(num2));
		
	}
}
